# 计算机内存：内存是计算机的重要部件，它用于暂时存放CPU中的运算数据，以及与硬盘等外部存储交换的数据。它是外存与CPU进行沟通的桥梁，计算机中所有程序的运行都在内存中运行
# 变量的概念：1.变量相当于内存中一个数据存储空间的表示 2.你可以把变量看做是一个房间的门牌号，通过门牌号我们可以找到房间，而通过变量名可以访问到变量（值）
# 1、不论是使用哪种高级程序语言编写程序，变量都是其程序的基本组成单位
# 2、变量有三个基本要素（类型+名称+值）
# 3、应用
